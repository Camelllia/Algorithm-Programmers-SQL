SELECT
    A.ANIMAL_ID,
    A.NAME
FROM
    (SELECT
        ANIMAL_ID,
        NAME,
        DATETIME
    FROM
        ANIMAL_INS
    ) AS A,
    (SELECT
        ANIMAL_ID,
        DATETIME
     FROM
        ANIMAL_OUTS
    ) AS B
WHERE
    A.DATETIME > B.DATETIME
AND
    A.ANIMAL_ID = B.ANIMAL_ID
ORDER BY
    A.DATETIME
    